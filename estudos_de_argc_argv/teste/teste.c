#include <unistd.h>
int main (int argc, char **argv)
{
	int i=0;
	int j=0;
	if (argc > 1)
	{
		while(i != argc)
		{
			j=0;
			while(argv[i][j] != '\0')
			{
				write(1 ,&argv[i][j], 1);
				j++;
			}
			write(1, " ", 1);
			i++;
		}
	}
	return (0);
}